//função que soma dois numeros
function somaDois(num1, num2) {
    return num1 + num2
}
console.log(somaDois(2, 2))

//função que calcula a autonommia de um veídulo - apartir da quantidade de combustivel e do seu redimento
function autonomia(quantidadeDeCombustivel, rendimento) {
    return quantidadeDeCombustivel * rendimento;
}
console.log(autonomia(10, 30));
